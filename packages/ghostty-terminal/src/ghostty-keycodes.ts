const UNSHIFTED_CODEPOINTS: Record<string, string> = {
  Backquote: '`',
  Backslash: '\\',
  BracketLeft: '[',
  BracketRight: ']',
  Comma: ',',
  Equal: '=',
  Minus: '-',
  Period: '.',
  Quote: "'",
  Semicolon: ';',
  Slash: '/',
  Space: ' ',
  IntlBackslash: '\\',
  IntlYen: '¥',
  IntlRo: '\\',
};

for (let index = 0; index <= 9; index += 1) {
  UNSHIFTED_CODEPOINTS[`Digit${index}`] = String(index);
}

for (let index = 0; index < 26; index += 1) {
  const uppercase = String.fromCharCode(65 + index);
  const lowercase = String.fromCharCode(97 + index);
  UNSHIFTED_CODEPOINTS[`Key${uppercase}`] = lowercase;
}

export function getGhosttyKeyCode(code: string): number {
  return KEY_CODE_MAP[code] ?? KEY_UNIDENTIFIED;
}

export function getUnshiftedCodepoint(code: string): number | null {
  const value = UNSHIFTED_CODEPOINTS[code];
  return value ? value.codePointAt(0) ?? null : null;
}
